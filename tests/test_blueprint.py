class FakeBlueprintService:
    def __init__(self, responses: list[str]):
        self.responses = responses
        self.calls: list[str] = []
        self.settings: list[object | None] = []

    async def get_chat_message_content(self, chat_history, settings=None):
        self.calls.append(chat_history.messages[-1].content or "")
        self.settings.append(settings)
        return ChatMessageContent(role=AuthorRole.ASSISTANT, content=self.responses.pop(0))


class RaisingBlueprintService:
    def __init__(self, exc: Exception):
        self.exc = exc
        self.calls = 0

    async def get_chat_message_content(self, chat_history, settings=None):
        self.calls += 1
        raise self.exc


@pytest.mark.asyncio
async def test_generate_blueprint_retries_invalid_json_then_succeeds():
    valid = {
        "schema_version": 1,
        "id": "bp_retry",
        "session_id": "s1",
        "name": "Retry Blueprint",
        "status": "draft",
        "problem_statement": "Problem",
        "target_user": "User",
        "use_cases": [],
        "non_goals": [],
        "input_contract": {"description": "Input", "examples": [], "required_fields": []},
        "output_contract": {"description": "Output", "format": "markdown", "required_sections": []},
        "workflow": {"steps": []},
        "agents": [],
        "tools": [],
        "evaluation": {"criteria": [], "test_cases": []},
        "risks": [],
        "exports": [],
        "generation": {"source": "model", "warnings": []},
    }
    service = FakeBlueprintService(["not json", json.dumps(valid)])

    result = await generate_blueprint(
        service=service,
        session_id="s1",
        topic="Idea",
        final_solution="Solution",
        dispatch_state={},
        discussion_transcript="Transcript",
    )

    assert result.blueprint.name == "Retry Blueprint"
    assert result.blueprint.generation.source == "retry"
    assert result.blueprint.generation.warnings == []
    assert "JSONDecodeError" in result.warnings[0]
    assert len(service.calls) == 2
    assert all(settings is not None for settings in service.settings)


