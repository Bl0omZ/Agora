    assert blueprint.workflow.steps[0].name == "Input Normalization"
    assert blueprint.agents[0].name == "generator"
    assert blueprint.agents[0].goal == "Drafts ticket content"
    assert blueprint.risks[0].risk == "False positives"
    assert blueprint.risks[0].severity == "high"
